public class FormasImpressoes {
    public static void main(String[] args){
        int x = 2;
        int y = 3;
        int a = 1;
        int b = 2;
        int c = 3;
        int d = 4;

        System.out.printf("%d = %d%n", (x + y), (y + x));
        System.out.println(a + " " + b + " " + c + " " + d);
        System.out.print(a + " " );
        System.out.print(b + " " );
        System.out.print(c + " " );
        System.out.print(d);
        System.out.printf("\n %d %d %d %d", a, b, c, d);

    }

}
